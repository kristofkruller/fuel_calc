package com.iec.fuel_calculator.model;

public class CalculatedData {
	private double calcConsumption;
	private double calcPrice;
	
	public double getCalcConsumption() {
		return calcConsumption;
	}
	
	public void setCalcConsumption(double calcConsumption) {
		this.calcConsumption = calcConsumption;
	}
	
	public double getCalcPrice() {
		return calcPrice;
	}
	
	public void setCalcPrice(double calcPrice) {
		this.calcPrice = calcPrice;
	}
}
